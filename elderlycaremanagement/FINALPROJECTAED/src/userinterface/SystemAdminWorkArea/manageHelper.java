    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for(Helper helper : ecosystem.getHelperDirectory().getHelperDirectory()){
            Object [] row = new Object[6];
            row[0] = helper;
            row[1] = helper.getName();
            row[2] = helper.getProfession();
            row[3] = helper.getMobile();
            row[4] = helper.getEmail();
            row[5] = helper.getAddress();
            
            model.addRow(row);
        }
    }

