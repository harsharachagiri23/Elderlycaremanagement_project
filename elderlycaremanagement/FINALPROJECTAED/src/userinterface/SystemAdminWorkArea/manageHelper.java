    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
            int selectedRow = jTable1.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,"Please Select a row to update");
            return;
        }
        Helper help = (Helper)jTable1.getValueAt(selectedRow,0);
        
        updateHelper updateHelper = new updateHelper(userProcessContainer, ecosystem, helperDirectory,help);
        userProcessContainer.add("updtaeHelperJPanel",updateHelper);
        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
         int selectedRow = jTable1.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,"Select a row to delete");
            return;
        }
        
        Helper helper = (Helper) jTable1.getValueAt(selectedRow, 0);
        helperDirectory.removeHelper(helper);
        populateTable();
    }//GEN-LAST:event_deleteButtonActionPerformed

