    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
       String mobile = mobileText.getText();
        String email = emailText1.getText();
        String userName = UIText.getText();
        String password = pwdText.getText();
        
        
         for(Helper helper : helperDirectory.getHelperDirectory()) {
           
            if(helper.getMobile().equals(mobile)) {
                JOptionPane.showMessageDialog(null, "Mobile Number already exists");
            }
        }
         for(Helper helper : helperDirectory.getHelperDirectory()) {
           
            if(helper.getEmail().equals(email)) {
                JOptionPane.showMessageDialog(null, "Email already exists");
            }
        }
         
            for(UserAccount account : ecosystem.getUserAccountDirectory().getUserAccountList()) {
            if(account.getUsername().equals(userName)) {
                JOptionPane.showMessageDialog(null, "Username Already exists");
                return;
            }
        }
        
        if (nameText1.getText().isEmpty() || !nameText1.getText().matches("^[a-zA-Z]+(?:[\\s-][a-zA-Z]+)*$"))
          JOptionPane.showMessageDialog(null, "Please enter valid name");
        
        
        else if(!(mobileText.getText().length()==10) || !mobileText.getText().matches("^[0-9]+") || mobileText.getText().matches("[0]{10}"))
        JOptionPane.showMessageDialog(null, "enter valid mobile number");
        
      
       
        else if(emailText1.getText().isEmpty()|| !(emailText1.getText().trim().contains("@") && emailText1.getText().trim().contains(".")))
          JOptionPane.showMessageDialog(null, "Enter valid email address");
        
        else if (priceText.getText().length() == 0){
          JOptionPane.showMessageDialog(null, "Please enter valid price");
        }
       
        else if (profText.getText().isEmpty()){
          JOptionPane.showMessageDialog(null, "Please enter address");
        }
        
        else if (userName.isEmpty() || !userName.matches("^[a-zA-Z0-9]*$"))
          JOptionPane.showMessageDialog(null, "Please enter userName");
        
        else if (password.isEmpty() || !password.matches("^[a-zA-Z0-9]*$"))
          JOptionPane.showMessageDialog(null, "Please enter password");
        
     
        else
           {
                   JOptionPane.showMessageDialog(null, "Helper Profile Created");
                   //String userName,String profession,double price, String name, String email, String mobile, String address
        ecosystem.getHelperDirectory().addHelper(userName,profText.getText(),Double.parseDouble(priceText.getText()),nameText1.getText(), email, mobile, addText.getText());
        Employee employee = ecosystem.getEmployeeDirectory().createEmployee(nameText1.getText());
        UserAccount userAccount = ecosystem.getUserAccountDirectory().createUserAccount(userName, password, employee, new HelperRole());
           }
        
    }//GEN-LAST:event_addButtonActionPerformed

