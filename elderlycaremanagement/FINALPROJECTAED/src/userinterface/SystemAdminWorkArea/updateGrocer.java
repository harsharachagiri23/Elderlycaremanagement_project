    private void modifyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifyButtonActionPerformed
        // TODO add your handling code here:
        String mobile = mobileText.getText();
        String address = addressText.getText();
        String name = nameText.getText();
        if(nameText.getText().isEmpty() || mobile.isEmpty() ||
        managerText.getText().isEmpty() || address.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Fields cannot be left empty");
        return;
        }
        for(Grocer grocer : grocerDirectory.getGrocerDirectory()) {

            if(grocer.getPhoneNum().equals(mobile) && this.grocer != grocer) {
                JOptionPane.showMessageDialog(null, "Mobile Number already exists");
            }
        }


        if (nameText.getText().isEmpty() || !nameText.getText().matches("^[a-zA-Z]+(?:[\\s-][a-zA-Z]+)*$"))
        JOptionPane.showMessageDialog(null, "Please enter valid name");

        else if(!(mobileText.getText().length()==10) || !mobileText.getText().matches("^[0-9]+") || mobileText.getText().matches("[0]{10}"))
        JOptionPane.showMessageDialog(null, "enter valid mobile number");

        else if (address.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter address");
        }


        else
        {
            JOptionPane.showMessageDialog(null, "Grocer profile modified");

            grocer.setAddress(address);
            grocer.setGrocerName(name);
            grocer.setManagerName(managerText.getText());
            grocer.setPhoneNum(mobile);
           // UserAccount userAccount = ecosystem.getUserAccountDirectory().createUserAccount(userName, password, employee, new GrocerRole());
        }
    }//GEN-LAST:event_modifyButtonActionPerformed

